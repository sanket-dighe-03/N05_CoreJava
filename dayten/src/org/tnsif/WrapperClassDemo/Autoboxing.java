package org.tnsif.WrapperClassDemo;
/*program to demonstate on autoboxing
 * Autoboxing is nothing but the conversion of primitive type to object type*/

public class Autoboxing {

	public static void main(String[] args) {
		char c='A';
		//converting primitive datatype into object type
		Character ch=c;
		System.out.println(ch);
		
		

	}

}
